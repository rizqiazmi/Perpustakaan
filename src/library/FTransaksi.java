    public void ShowDataBuku(String where){
        try{
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM `tbl_buku` WHERE jumlahBuku > 0" );
            
            while (rs.next()){
                Object[] data = {
                    rs.getString("kodeBuku"),
                    rs.getString("judulBuku"),
                };
                tabModel.addRow(data);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void ShowDataMember(String where){
        try{
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM `tbl_anggota` ORDER BY `id_anggota` ASC" + where);
            
            while (rs.next()){
                Object[] data = {
                    rs.getString("nim"),
                    rs.getString("nama"),
                };
                tabModel.addRow(data);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void ShowDataTransaksi(String where){
        try{
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM `tbl_transaksi`" + where);
            
            while (rs.next()){
                Object[] data = {
                    rs.getString("kodeTransaksi"),
                    rs.getString("peminjam"),
                    rs.getString("buku"),
                    rs.getString("tglAwalPinjam"),
                    rs.getString("batasPinjam"),
                    rs.getString("denda"),
                    rs.getString("status"),
                };
                tabModel.addRow(data);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    public static String generateRandomChars(String candidateChars, int length) {
    StringBuilder sb = new StringBuilder();
    Random random = new Random();
    for (int i = 0; i < length; i++) {
        sb.append(candidateChars.charAt(random.nextInt(candidateChars.length())));
    }
    return sb.toString();
}
    
