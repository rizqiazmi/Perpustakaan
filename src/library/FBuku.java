    private void buttonBuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuatActionPerformed
        // TODO add your handling code here:
         if(inputKode.getText().equals("")&&inputJudul.getText().equals("")&&inputPengrang.getText().equals("")&&inputPenerbit.getText().equals("")&&inputGenre.getText().equals("")&&inputJumlah.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Salah satu atau lebih data masih kosong, mohon cek kembail");
        }else{
        try{
            st = cn.createStatement();
            st.executeUpdate("INSERT INTO `tbl_buku` VALUES ('"+inputKode.getText()+"','"+inputJudul.getText()+"','"+inputPengrang.getText()+"','"+inputPenerbit.getText()+"','"+inputGenre.getText()+"','"+inputJumlah.getText()+"')");
            ShowData("");
            JOptionPane.showMessageDialog(null,"Simpan Berhasil");
            inputKode.setText("");
            inputJudul.setText("");
            inputPengrang.setText("");
            inputPenerbit.setText("");
            inputGenre.setText("");
            inputJumlah.setText("");
        }catch(Exception e){
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_buttonBuatActionPerformed

    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        // TODO add your handling code here:
        if(inputKode.getText().equals("")&&inputJudul.getText().equals("")&&inputPengrang.getText().equals("")&&inputPenerbit.getText().equals("")&&inputGenre.getText().equals("")&&inputJumlah.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Salah satu atau lebih data masih kosong, mohon cek kembail");
        }else{
        try{
            st = cn.createStatement();
            st.executeUpdate("UPDATE `tbl_buku` SET "
                    +"kodeBuku='"+inputKode.getText()+"',"
                    +"judulBuku='"+inputJudul.getText()+"',"
                    +"pengarangBuku='"+inputPengrang.getText()+"',"
                    +"penerbitBuku='"+inputPenerbit.getText()+"',"
                    +"genreBuku='"+inputGenre.getText()+"',"
                    +"jumlahBuku='"+inputJumlah.getText()+""
                    +"'WHERE kodeBuku='"+inputKode.getText()+"'");
            ShowData("");
            JOptionPane.showMessageDialog(null,"Perubahan Tersimpan");
            reset();
        }catch(Exception e){
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_buttonUpdateActionPerformed

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        // TODO add your handling code here:
        try{
            int confirm;
            if((confirm = JOptionPane.showConfirmDialog(null,"Apakah anda mau menghapus data ini?","Konfirmasi",JOptionPane.YES_NO_OPTION))==0){
                st = cn.createStatement();
                st.executeUpdate("DELETE FROM `tbl_buku` WHERE kodeBuku='"+tabModel.getValueAt(tableBuku.getSelectedRow(), 0)+"'");
                ShowData("");
                reset();
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_buttonDeleteActionPerformed

